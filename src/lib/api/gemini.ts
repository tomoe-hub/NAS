interface ParsedArticleResult {
  title: string
  content: string
}

/**
 * モデルが「出力形式」説明文を混ぜて返した場合でも、実データに近い
 * タイトル＋本文ブロックを抽出する。
 */
function parseGeneratedArticleText(text: string): ParsedArticleResult {
  const normalized = text.replace(/\r\n/g, '\n')
  const lines = normalized.split('\n')
  let best: ParsedArticleResult | null = null

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i]!.trim()
    const m = line.match(/^タイトル[:：]\s*(.+)\s*$/)
    if (!m) continue
    const candidateTitle = m[1]!.trim()
    if (!candidateTitle || /（記事タイトル）/.test(candidateTitle)) continue

    // --- 区切りをタイトル行から最大20行先まで探す（空白行が多くても対応）
    let sepIdx = -1
    for (let j = i + 1; j <= Math.min(i + 20, lines.length - 1); j++) {
      if (/^\s*-{3,}\s*$/.test(lines[j]!)) {
        sepIdx = j
        break
      }
    }
    if (sepIdx === -1) continue

    const candidateContent = lines.slice(sepIdx + 1).join('\n').trim().replace(/^-\s*/, '')
    if (!candidateContent) continue
    if (!best || candidateContent.length > best.content.length) {
      best = { title: candidateTitle, content: candidateContent }
    }
  }

  if (best) return best

  // フォールバック: タイトル行を正規表現で抽出、プレースホルダーは除外
  const fallbackTitleMatch = normalized.match(/タイトル[:：]\s*([^（\n][^\n]+)/)
  const rawFallbackTitle = fallbackTitleMatch?.[1]?.trim() ?? ''
  const isPlaceholder = !rawFallbackTitle || /（記事タイトル）/.test(rawFallbackTitle)
  const fallbackTitle = isPlaceholder ? '' : rawFallbackTitle

  // タイトル行の末尾位置を特定
  const titleLineEnd = fallbackTitleMatch
    ? normalized.indexOf('\n', normalized.indexOf(fallbackTitleMatch[0]))
    : -1

  if (titleLineEnd >= 0) {
    // タイトル行以降の全テキストを本文として取得する。
    // これにより「本文の途中に --- がある（FAQ前など）」ケースでも
    // FAQ のみが content になるバグを防ぐ。
    let bodyText = normalized.slice(titleLineEnd + 1).trim()
    // タイトル直後の --- は出力フォーマットの区切り文字なので除去
    bodyText = bodyText.replace(/^\s*-{3,}\s*\n?/, '')
    return { title: fallbackTitle, content: bodyText }
  }

  // titleLineEnd が取れない最終フォールバック
  const fallbackSep = normalized.indexOf('---')
  const fallbackContent = fallbackSep >= 0
    ? normalized.slice(fallbackSep + 3).trim().replace(/^-\s*/, '')
    : normalized.replace(/タイトル[:：].+/, '').trim()

  return { title: fallbackTitle, content: fallbackContent }
}

const TITLE_MARKER = '<<<TITLE>>>'
const BODY_MARKER = '<<<BODY>>>'

/**
 * マーカー方式（<<<TITLE>>> / <<<BODY>>>）でタイトル・本文を抽出する。
 * マーカーが見つからない場合は旧形式（タイトル：…\n---\n本文）のパーサーへ、
 * それも失敗した場合は「全文を本文」として返す（throwしない）。
 */
function parseArticleOutput(text: string): ParsedArticleResult {
  const normalized = text.replace(/\r\n/g, '\n')

  const titleIdx = normalized.indexOf(TITLE_MARKER)
  const bodyIdx = normalized.indexOf(BODY_MARKER)

  if (titleIdx !== -1 && bodyIdx !== -1 && bodyIdx > titleIdx) {
    const rawTitle = normalized
      .slice(titleIdx + TITLE_MARKER.length, bodyIdx)
      .trim()
      // 「タイトル：」を重ねて書いてくるケースを吸収
      .replace(/^タイトル[:：]\s*/, '')
      // タイトルが複数行になった場合は1行目のみ
      .split('\n')[0]!
      .trim()
    const rawBody = normalized.slice(bodyIdx + BODY_MARKER.length).trim()
    const title = /（記事タイトル）|（推敲後タイトル）/.test(rawTitle) ? '' : rawTitle
    if (rawBody) {
      return { title, content: rawBody }
    }
  }

  // 本文側だけマーカーがあるケース（タイトルマーカー欠落）
  if (bodyIdx !== -1) {
    const rawBody = normalized.slice(bodyIdx + BODY_MARKER.length).trim()
    const beforeBody = normalized.slice(0, bodyIdx)
    const titleMatch = beforeBody.match(/タイトル[:：]\s*([^\n]+)/)
    const title = titleMatch?.[1]?.trim() ?? ''
    if (rawBody) {
      return {
        title: /（記事タイトル）|（推敲後タイトル）/.test(title) ? '' : title,
        content: rawBody,
      }
    }
  }

  // 旧形式（タイトル：…\n---\n本文）フォールバック
  return parseGeneratedArticleText(normalized)
}

