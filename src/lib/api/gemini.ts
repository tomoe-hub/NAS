/**
 * generateContent を順に試すモデル。
 * gemini-1.5-* は 2025年9月に廃止済みのため 2.x 系のみを候補にする。
 * 404（モデル不存在）→ 次モデルへ、429/503 → 同一モデル待機再試行、最終失敗 → Claude(Bedrock)。
 * 上書き: 環境変数 GEMINI_MODEL_LIST にカンマ区切り（例: gemini-2.5-flash,gemini-2.0-flash）
 */
const DEFAULT_GEMINI_MODELS = [
  'gemini-2.5-flash',
  'gemini-2.5-flash-lite',
  'gemini-2.0-flash',
  'gemini-2.0-flash-lite',
] as const

function getGeminiModelIds(): readonly string[] {
  const raw = process.env.GEMINI_MODEL_LIST?.trim()
  if (!raw) return DEFAULT_GEMINI_MODELS
  const parsed = raw.split(',').map((s) => s.trim()).filter(Boolean)
  return parsed.length > 0 ? parsed : DEFAULT_GEMINI_MODELS
}

/** SDK / Google 側のエラーをログ・ユーザー向け詳細用に1行にまとめる */
function formatGeminiCaughtError(e: unknown): string {
  if (e instanceof Error) {
    const withCause = e as Error & { cause?: unknown }
    const tail =
      withCause.cause !== undefined && withCause.cause !== null
        ? ` | cause: ${formatGeminiCaughtError(withCause.cause)}`
        : ''
    return `${e.message}${tail}`
  }
  if (e && typeof e === 'object' && 'message' in e && typeof (e as { message: unknown }).message === 'string') {
    return (e as { message: string }).message
  }
  try {
    return JSON.stringify(e)
  } catch {
    return String(e)
  }
}

function sleep(ms: number): Promise<void> {
  return new Promise(resolve => setTimeout(resolve, ms))
}

function readPositiveIntEnv(name: string, fallback: number): number {
  const raw = process.env[name]?.trim()
  if (!raw) return fallback
  const n = parseInt(raw, 10)
  return Number.isFinite(n) && n > 0 ? n : fallback
}

/** Google 429 応答の「Please retry in 24.9s」や RetryInfo から待機秒を読む */
function parseRetryDelaySeconds(detail: string): number | null {
  const m1 = detail.match(/Please retry in ([\d.]+)\s*s/i)
  if (m1) {
    const sec = parseFloat(m1[1]!)
    if (Number.isFinite(sec) && sec >= 0) return Math.min(sec, 120)
  }
  const m2 = detail.match(/"retryDelay"\s*:\s*"(\d+)s"/i)
  if (m2) {
    const sec = parseInt(m2[1]!, 10)
    if (Number.isFinite(sec) && sec >= 0) return Math.min(sec, 120)
  }
  return null
}

/**
 * Gemini の候補モデルを順に試して generateContent を実行する。
 * - 429（quota）・503（high demand）時は短い待機で同一モデルを再試行し、
 *   それでもダメなら次モデルへフォールバック。
 * - 404（モデル不存在・generateContent 非対応）は待機せず即時次モデルへ。
 * - 全候補が失敗した場合は最後のエラー詳細を含めて throw する。
 */
async function tryGeminiModels(apiKey: string, prompt: string): Promise<string> {
  const genAI = new GoogleGenerativeAI(apiKey)
  const maxRetriesPerModel = 2
  // Vercel の関数タイムアウト(504)回避のため、Gemini 側の試行時間に上限を設ける
  const totalBudgetMs = readPositiveIntEnv('GEMINI_TOTAL_BUDGET_MS', 55_000)
  const maxRetryWaitMs = readPositiveIntEnv('GEMINI_MAX_RETRY_WAIT_MS', 8_000)
  const startedAt = Date.now()
  let lastError: Error | null = null
  let lastDetail = ''

  const modelIds = getGeminiModelIds()
  for (const modelId of modelIds) {
    for (let attempt = 0; attempt < maxRetriesPerModel; attempt++) {
      if (Date.now() - startedAt >= totalBudgetMs) {
        console.warn('[Gemini] 総予算時間を超過したため、次の経路へ切り替えます')
        break
      }
      try {
        const model = genAI.getGenerativeModel({ model: modelId })
        const result = await model.generateContent(prompt)
        return result.response.text()
      } catch (e) {
        lastError = e instanceof Error ? e : new Error(String(e))
        lastDetail = formatGeminiCaughtError(e)
        console.error(`[Gemini] model=${modelId} attempt=${attempt + 1}/${maxRetriesPerModel} 失敗:`, lastDetail)

        const combined = `${lastError.message}\n${lastDetail}`

        const isModelUnavailable =
          /404|NOT_FOUND|not found for API version|is not supported for generateContent|ListModels|is not available|deprecat/i.test(combined)
        if (isModelUnavailable) {
          console.warn(`[Gemini] model=${modelId} は利用不可のためスキップして次モデルを試します`)
          break
        }

        // 1日クォータ超過（429 Quota Exceeded）は同一モデルで待機しても無意味なため
        // 即座に次のモデルへスキップする
        const isQuotaExhausted =
          /429|quota exceeded|Too Many Requests|RESOURCE_EXHAUSTED|Quota exceeded for metric.*free_tier/i.test(combined)
        if (isQuotaExhausted) {
          console.warn(`[Gemini] model=${modelId} クォータ超過のため次のモデルへスキップします`)
          break
        }

        const isRetryable =
          /Resource exhausted|503|Service Unavailable|high demand|Overloaded/i.test(combined)

        if (!isRetryable) {
          // 非クォータ系の未分類エラーはループを抜けて次の経路へ
          break
        }

        const canRetry = attempt < maxRetriesPerModel - 1
        if (canRetry) {
          const waitSec = parseRetryDelaySeconds(lastDetail)
          const requestedMs = waitSec != null ? Math.ceil(waitSec * 1000) + 800 : 5_000
          const ms = Math.min(requestedMs, maxRetryWaitMs)
          const elapsed = Date.now() - startedAt
          const remaining = totalBudgetMs - elapsed
          if (remaining <= 1_000) {
            console.warn('[Gemini] 残り時間不足のため待機再試行をスキップし、次の経路へ進みます')
            break
          }
          const boundedMs = Math.min(ms, Math.max(0, remaining - 700))
          if (boundedMs <= 0) {
            console.warn('[Gemini] タイムバジェット上、待機再試行できないため次へ進みます')
            break
          }
          console.warn(`[Gemini] ${ms}ms 待機して再試行します (${modelId})`)
          await sleep(boundedMs)
          continue
        }
        console.warn(`[Gemini] ${modelId} で${maxRetriesPerModel}回失敗。次のモデルへフォールバック`)
        break
      }
    }
  }

  console.error('[Gemini] 全候補モデルで失敗', { models: [...modelIds], lastDetail })
  const suffix = lastDetail ? ` [Gemini詳細: ${lastDetail}]` : ''
  throw lastError ?? new Error(`Gemini 生成に失敗しました${suffix}`)
}

/** Claude（Bedrock）で生成する。未設定なら例外。 */
async function tryClaude(prompt: string): Promise<string> {
  if (!isClaudeConfigured()) {
    throw new Error('Claude（Bedrock）が未設定です（AWS 認証情報を確認してください）')
  }
  const claudeText = await generateWithClaude(prompt, {
    maxTokens: 8000,
    temperature: 0.7,
  })
  console.log(`[Claude] 生成成功（文字数: ${claudeText.length}）`)
  return claudeText
}

/**
 * 記事生成の本体。既定では Claude（Bedrock / Sonnet 4.6）を主エンジンとして使い、
 * 失敗時に Gemini へフォールバックする。
 * 環境変数 AI_PRIMARY=gemini を指定すると旧来の Gemini 優先に戻せる。
 */
async function generateContentWithFallback(apiKey: string, prompt: string): Promise<string> {
  const primary = (process.env.AI_PRIMARY?.trim().toLowerCase() || 'claude')

  if (primary === 'gemini') {
    // 旧来順序: Gemini → Claude
    try {
      return await tryGeminiModels(apiKey, prompt)
    } catch (geminiErr) {
      const geminiDetail = geminiErr instanceof Error ? geminiErr.message : String(geminiErr)
      if (!isClaudeConfigured()) {
        throw new Error(
          'Gemini API が一時的に利用できません（サーバー混雑またはクォータ超過）。' +
            `しばらく待ってから再度お試しください。 [詳細: ${geminiDetail}]`,
        )
      }
      try {
        console.warn('[Gemini→Claude] Gemini 失敗のため Claude にフォールバックします')
        return await tryClaude(prompt)
      } catch (claudeErr) {
        const claudeDetail = claudeErr instanceof Error ? claudeErr.message : String(claudeErr)
        throw new Error(`Gemini・Claude 両方の生成に失敗しました。Claude詳細: ${claudeDetail} [Gemini詳細: ${geminiDetail}]`)
      }
    }
  }

  // 既定順序: Claude（主）→ Gemini（予備）
  try {
    return await tryClaude(prompt)
  } catch (claudeErr) {
    const claudeDetail = claudeErr instanceof Error ? claudeErr.message : String(claudeErr)
    console.warn('[Claude→Gemini] Claude 失敗のため Gemini にフォールバックします:', claudeDetail)
    try {
      return await tryGeminiModels(apiKey, prompt)
    } catch (geminiErr) {
      const geminiDetail = geminiErr instanceof Error ? geminiErr.message : String(geminiErr)
      throw new Error(`Claude・Gemini 両方の生成に失敗しました。Claude詳細: ${claudeDetail} [Gemini詳細: ${geminiDetail}]`)
    }
  }
}

